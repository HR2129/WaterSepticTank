export const convertEngNumToMarathi = (number) => {
  const marathiDigits = ["реж", "рез", "реи", "рей", "рек", "рел", "рем", "рен", "рео", "реп"];
  return number
    .toString()
    .split("")
    .map((char) => (/\d/.test(char) ? marathiDigits[parseInt(char)] : char))
    .join("");
};

