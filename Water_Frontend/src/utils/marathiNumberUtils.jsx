const higherDigitLabels = ["", "рд╣рдЬрд╛рд░", "рд▓рд╛рдЦ", "рдХреЛрдЯреА"];

export const marathiNumberToWords = (num) => {
  if (num === 0) return "рд╢реВрдиреНрдп";

  const getTwoDigitWord = (n) => {
    if (n < 100) return hundredHindiDigitArray[n];
    return "";
  };

  const getThreeDigitWord = (n) => {
    if (n === 0) return "";

    if (n < 100) return getTwoDigitWord(n);

    const hundreds = Math.floor(n / 100);
    const remainder = n % 100;

    const hundredWord = hundredHindiDigitArray[hundreds] + "рд╢реЗ "; // ЁЯСИ joined with "рд╢реЗ"
    const remainderWord = getTwoDigitWord(remainder);

    return hundredWord + (remainderWord ? remainderWord : "");
  };

  const parts = num.toString().split(".");
  let intPart = parseInt(parts[0], 10);
  let fracPart = parts[1] ? parseInt(parts[1].padEnd(2, "0"), 10) : 0;

  const crore = Math.floor(intPart / 10000000);
  const lakh = Math.floor((intPart % 10000000) / 100000);
  const thousand = Math.floor((intPart % 100000) / 1000);
  const hundred = intPart % 1000;

  let words = [];

  if (crore) words.push(getTwoDigitWord(crore) + " рдХреЛрдЯреА");
  if (lakh) words.push(getTwoDigitWord(lakh) + " рд▓рд╛рдЦ");
  if (thousand) words.push(getTwoDigitWord(thousand) + " рд╣рдЬрд╛рд░");
  if (hundred) words.push(getThreeDigitWord(hundred)); // ЁЯСИ no space in hundreds+twoDigit

  let finalWords = words.join(" ").trim();

  if (fracPart > 0) {
    finalWords += ` рдЖрдгрд┐ ${getTwoDigitWord(fracPart)} рдкреИрд╕реЗ`;
  }

  return finalWords + " рдлрдХреНрдд";
};
